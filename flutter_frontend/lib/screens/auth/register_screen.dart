                                  if (!RegExp(r'^[^@]+@[^@]+\.[^@]+')
                                      .hasMatch(value)) {
                                    return 'Please enter a valid email address';
                                  }
                                  return null;
                                },
                              ),

                              const SizedBox(height: 16),

                              // Emergency Email 3
                              TextFormField(
                                controller: _emergencyEmail3Controller,
                                style: const TextStyle(
                                  color: Colors.black87,
